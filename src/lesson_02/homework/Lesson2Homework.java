package lesson_02.homework;

public class Lesson2Homework {
    public static void main(String[] args) {
        //create 2 byte variables and print it
        byte firstByte = 1;
        byte secondByte = -11;
        System.out.println("First byte variable is " + firstByte);
        System.out.println("Second byte variable is " + secondByte);
        System.out.println("----");
        //create 2 short variables and print it
        short firstShort = 12345;
        short secondShort = -12344;
        System.out.println("firstShort = " + firstShort);
        System.out.println("secondShort = " + secondShort);
        System.out.println("----");
        //create 2 int variables and print it
        int firstInt = 1234567890;
        int secondInt = -987654321;
        System.out.println("firstInt = " + firstInt);
        System.out.println("secondInt = " + secondInt);
        System.out.println("----");
        //create 2 long variables and print it
        long firstLong = 1234567890987654321L;
        long secondLong = -987654321123456789L;
        System.out.println("firstLong = " + firstLong);
        System.out.println("secondLong = " + secondLong);
        //create 2 float variables and print it
        float firstFloat = 123.45F;
        float secondFloat = -66.6F;
        System.out.println("firstFloat = " + firstFloat);
        System.out.println("secondFloat = " + secondFloat);
        System.out.println("----");
        //create 2 double variables and print it
        double firstDouble = 1234542456.443454;
        double secondDouble = -231242343423.3453243;
        System.out.println("firstDouble = " + firstDouble);
        System.out.println("secondDouble = " + secondDouble);
        System.out.println("----");
        //create 2 boolean variables and print it
        boolean firstBoolean = true;
        boolean secondBoolean = false;
        System.out.println("firstBoolean = " + firstBoolean);
        System.out.println("secondBoolean = " + secondBoolean);
        System.out.println("----");
        //create char variables and print it
        char letterA = 'A';
        char unicodeLetterA = '\u0041';
        char numberLetterA = 65;
        Character objectLetterA = new Character('a');
        System.out.println("letterA = " + letterA);
        System.out.println("unicodeLetterA = " + unicodeLetterA);
        System.out.println("numberLetterA = " + numberLetterA);
        System.out.println("objectLetterA = " + objectLetterA);

    }
}
